    DESKRIPSI CURUG IBUN PELANGI
   </h2>
   <p>
    Curug Ibun Pelangi biasanya disebut sebagai Green Canyon nya Majalengka. Objek wisata berupa curug diapit oleh dua tebing batu besar setinggi kurang lebih 20 meter. Pemandangan terlihat eksotis dengan dinding batu lava yang menjulang ke atas. Udara disini pun terasa sejuk alami.
   </p>
   <p>
    Pengunjung yang ingin melihat keindahan Curug Ibun Pelangi akan dikenakan biaya masuk sebesar Rp 10.000 dengan biaya parkir sekitar Rp 5.000.
   </p>
   <p>
    Jarak tempuh Curug Ibun Pelangi dari pusat Majalengka sekitar 18 km. Dengan waktu tempuh sekitar 30 menit. Waktu tempuh perjalanan dari terminal Maja sekitar 5 hingga 10 menit untuk tiba di Curug Ibun Pelangi. Untuk sampai ke Curug, pengunjung harus menuruni sekitar 50 anak tangga yang sangat curam. Tetapi perjalanan ini pasti akan terbayar dengan segarnya gemericik air curug dan kesejukan disertai titik-titik air Curug Ibun Pelangi.
